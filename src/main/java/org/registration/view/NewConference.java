package org.registration.view;

import java.util.List;

public class NewConference {

	private String conferenceName;
	private String registrationStartDate;
	private String registrationEndDate;
	private String abstractStartDate;
	private String abstractEndDate;
	private String emailList;
	private String confirmationEmail;
	private boolean shortTalks;
	
	public String getConferenceName() {
		return conferenceName;
	}
	public void setConferenceName(String conferenceName) {
		this.conferenceName = conferenceName;
	}
	public String getRegistrationStartDate() {
		return registrationStartDate;
	}
	public void setRegistrationStartDate(String registrationStartDate) {
		this.registrationStartDate = registrationStartDate;
	}
	public String getRegistrationEndDate() {
		return registrationEndDate;
	}
	public void setRegistrationEndDate(String registrationEndDate) {
		this.registrationEndDate = registrationEndDate;
	}
	public String getAbstractEndDate() {
		return abstractEndDate;
	}
	public void setAbstractEndDate(String abstractEndDate) {
		this.abstractEndDate = abstractEndDate;
	}
	public String getEmailList() {
		return emailList;
	}
	public void setEmailList(String emailList) {
		this.emailList = emailList;
	}
	public String getConfirmationEmail() {
		return confirmationEmail;
	}
	public void setConfirmationEmail(String confirmationEmail) {
		this.confirmationEmail = confirmationEmail;
	}
	public boolean isShortTalks() {
		return shortTalks;
	}
	public void setShortTalks(boolean shortTalks) {
		this.shortTalks = shortTalks;
	}
	public String getAbstractStartDate() {
		return abstractStartDate;
	}
	public void setAbstractStartDate(String abstractStartDate) {
		this.abstractStartDate = abstractStartDate;
	}
		
}
